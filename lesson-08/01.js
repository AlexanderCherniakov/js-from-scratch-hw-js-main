// –§—É–Ω–∫—Ü–∏—è, –ø—Ä–µ–æ–±—Ä–∞–∑—É—é—â–∞—è –ø–µ—Ä–≤—É—é –±—É–∫–≤—É —Å–ª–æ–≤–∞ –≤ –≤–µ—Ä—Ö–Ω–∏–π —Ä–µ–≥–∏—Å—Ç—Ä
const toUpperCaseFirstLetter = word => {
  return word.charAt(0).toUpperCase() + word.slice(1);
};

// –û—Å–Ω–æ–≤–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è, –æ–±—Ä–∞–±–∞—Ç—ã–≤–∞—é—â–∞—è –≤—Å—é —Å—Ç—Ä–æ–∫—É
const capitalizeWords = str => {
  // –†–∞–∑–±–∏–≤–∞–µ–º —Å—Ç—Ä–æ–∫—É –Ω–∞ –º–∞—Å—Å–∏–≤ —Å–ª–æ–≤
  const wordsArray = str.split(' ');

  // –ü—Ä–∏–º–µ–Ω—è–µ–º –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏–µ –∫ –∫–∞–∂–¥–æ–º—É —Å–ª–æ–≤—É –º–∞—Å—Å–∏–≤–∞
  const capitalizedWords = wordsArray.map(toUpperCaseFirstLetter);

  // –û–±—ä–µ–¥–∏–Ω—è–µ–º –æ–±—Ä–∞—Ç–Ω–æ –≤ —Å—Ç—Ä–æ–∫—É
  return capitalizedWords.join(' ');
};

// –ü—Ä–∏–º–µ—Ä –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è —Ñ—É–Ω–∫—Ü–∏–∏
console.log(capitalizeWords("hello world from javascript")); // Hello World From JavaScript
